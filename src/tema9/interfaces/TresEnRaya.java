    private void jButtonActionPerformed(java.awt.event.ActionEvent evt, int fila, int columna) {                                         
        JButton buttonClicked = (JButton) evt.getSource();
        if (buttonClicked.getText().equals("")) {
            buttonClicked.setText(turnoJugador1 ? "X" : "O");
            tablero[fila][columna] = buttonClicked.getText();
            turnoJugador1 = !turnoJugador1;
            jTextArea1.setText("Turno del jugador: " + (turnoJugador1 ? "X" : "O"));
            verificarGanador();
        }
    }                                        

    private void verificarGanador() {
        for (int i = 0; i < 3; i++) {
            if (verificarLinea(tablero[i][0], tablero[i][1], tablero[i][2])) return;
            if (verificarLinea(tablero[0][i], tablero[1][i], tablero[2][i])) return;
        }
        if (verificarLinea(tablero[0][0], tablero[1][1], tablero[2][2])) return;
        if (verificarLinea(tablero[0][2], tablero[1][1], tablero[2][0])) return;

        boolean empate = true;
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (tablero[i][j].equals("")) {
                    empate = false;
                    break;
                }
            }
        }
        if (empate) {
            jTextArea1.setText("Empate. ¬°Juego terminado!");
        }
    }

    private boolean verificarLinea(String s1, String s2, String s3) {
        if (!s1.equals("") && s1.equals(s2) && s2.equals(s3)) {
            jTextArea1.setText("Jugador " + s1 + " gana. ¬°Juego terminado!");
            return true;
        }
        return false;
    }
